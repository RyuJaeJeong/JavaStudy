public class Sample01 {
	public static void main(String[] args) {
		char chr = 'A';
		System.out.println("Char : " + chr);			//문자
		System.out.println("Encoding : " + (int)chr);	//ASCII 코드로
		System.out.println("Decoding : " + (char)65);	//문자로 디코딩
		
		
		
	}
}
